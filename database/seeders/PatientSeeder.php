/**
 * PatientSeeder
 *
 * Seeds 30 realistic breast cancer patients derived from TCGA-BRCA
 * clinical data patterns. All values are anonymised and representative
 * of real clinical distributions.
 *
 * Run: php artisan db:seed --class=PatientSeeder
 * Or add to DatabaseSeeder::run() after org/user setup.
