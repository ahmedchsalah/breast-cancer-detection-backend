    public function run(): void
    {
        // Find the first active organization to attach patients to
        $org = Organization::where('status', 'active')->first();

        if (!$org) {
            $this->command->warn('No active organization found. Skipping PatientSeeder.');
            return;
        }

        $patients = $this->getPatientData();

        $created = 0;
        foreach ($patients as $p) {
            // Skip if identifier already exists
            if (Patient::where('patient_identifier', $p['patient_identifier'])->exists()) {
                continue;
            }

            Patient::create([
                'organization_id'         => $org->id,
                'patient_identifier'      => $p['patient_identifier'],
                'er_status'               => $p['er_status'],
                'pr_status'               => $p['pr_status'],
                'her2_binary'             => $p['her2_binary'],
                'age'                     => $p['age'],
                'stage_num'               => $p['stage_num'],
                'er_status_missing'       => $p['er_status_missing'] ?? false,
                'pr_status_missing'       => $p['pr_status_missing'] ?? false,
                'fraction_genome_altered' => $p['fraction_genome_altered'] ?? null,
                'buffa_hypoxia_score'     => $p['buffa_hypoxia_score'] ?? null,
                'ragnum_hypoxia_score'    => $p['ragnum_hypoxia_score'] ?? null,
                'winter_hypoxia_score'    => $p['winter_hypoxia_score'] ?? null,
            ]);
            $created++;
        }

        $this->command->info("PatientSeeder: {$created} patients created for org '{$org->name}'.");
    }

